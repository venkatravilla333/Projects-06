import mongoose from 'mongoose'


var userSchema = mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  cpassword: {
    type: String,
    required: true
  }
}, { timeStamps: true })

export var User = mongoose.model('users', userSchema)




